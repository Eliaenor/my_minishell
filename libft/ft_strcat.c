#include "./includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		n;

	i = 0;
	n = 0;
	while (s1[n] != '\0')
	{
		n++;
	}
	while (s2[i] != '\0')
	{
		s1[n] = s2[i];
		i++;
		n++;
	}
	s1[n] = '\0';
	return (s1);
}
