#include "./includes/libft.h"

void	ft_putendl(char const *s)
{
	if (s == NULL)
		write(1, "NULL", 5);
	while (*s != '\0')
	{
		write(1, s, 1);
		s++;
	}
	write(1, "\n", 1);
}
